import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';

interface Lead {
  id?: number;
  name: string;
  email: string;
  company: string;
  job_title: string;
  industry?: string;
  location?: string;
  score?: number;
  enriched_data?: object;
}

